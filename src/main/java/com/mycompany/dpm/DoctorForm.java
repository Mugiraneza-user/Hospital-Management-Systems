      private void sendMessage(){
        String fname = firstname.getText().trim();
        String lname = lastname.getText().trim();
        String disease = diseases.getText().trim();
        String patient = patients.getText().trim();
        String messages = message.getText().trim();
       
       
        // Validate inputs
        if (fname.isEmpty() || lname.isEmpty()|| messages.isEmpty() || patient.isEmpty()
              || disease.isEmpty()  ) {
            JOptionPane.showMessageDialog(this,
                    "Please fill in all fields",
                    "Validation Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        try (Connection conn = DriverManager.getConnection(JDBC_URL, USERNAME, PASSWORD)) {
            String sql = "INSERT INTO Message (firstname, lastname,Diseases, PatientID, "
                    + "Message) "
                    + "VALUES (?, ?,?, ?,?)";

            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                pstmt.setString(1, fname);
                pstmt.setString(2, lname);
                pstmt.setString(3, disease);
                pstmt.setString(4, patient);
                pstmt.setString(5, messages);
                int rowsAffected = pstmt.executeUpdate();
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this,
                            "Message send successful!",
                            "Success",
                            JOptionPane.INFORMATION_MESSAGE);
                    clearForms();
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Failed to send message",
                            "Error",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,
                    "Database error: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
