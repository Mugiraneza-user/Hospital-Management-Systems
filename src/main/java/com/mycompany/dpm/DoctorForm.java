    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:

        String query = searchingg.getText().trim();

        if (query.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a search term!", "Input Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Perform the search and populate the table
            searchData(query);
        }
        }

        private void searchData(String query) {
            // Get the table's model
            DefaultTableModel tableModel = (DefaultTableModel) ResultTable.getModel(); // Ensure ResultTable is correctly initialized
            tableModel.setRowCount(0); // Clear previous data

            try {
                // Database connection details
                String url = "jdbc:mysql://localhost:3306/Hospital";
                String username = "root";
                String password = "";

                // Establish connection
                Connection connection = DriverManager.getConnection(url, username, password);

                // Prepare SQL query
                String sql = "SELECT * FROM appointments WHERE Date = ? OR patientID = ?";
                PreparedStatement stmt = connection.prepareStatement(sql);
                stmt.setString(1, query);
                stmt.setString(2, query);

                // Execute query and populate the table
                ResultSet rs = stmt.executeQuery();
                while (rs.next()) {
                    Object[] rowData = {
                        rs.getString("Firstname"),
                        rs.getString("Lastname"),
                        rs.getInt("patientID"),
                        rs.getString("Book"),
                        rs.getString("Diseases"),
                        rs.getString("Date")
                    };
                    tableModel.addRow(rowData);
                }

                // Close resources
                rs.close();
                stmt.close();
                connection.close();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Error accessing database: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_searchActionPerformed

